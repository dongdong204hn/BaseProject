#pragma mark - FirstResponder

@implementation UIView(NIPAdditions_FindFirstResponder)

- (UIView *)findFirstResponder {
    if (self.isFirstResponder) {
        return self;
    }
    __block UIView *subView = nil;
    
    [self.subviews enumerateObjectsWithOptions:NSEnumerationReverse usingBlock:^(__kindof UIView * _Nonnull obj, NSUInteger idx, BOOL * _Nonnull stop) {
        if ([obj isFirstResponder]) {
            subView = obj;
            *stop = YES;
        } else {
            subView = [obj findFirstResponder];
            if (subView) {
                *stop = YES;
            }
        }
    }];
    return subView;
}

- (UIViewController*)parentViewController {
    UIResponder *responder = self;
    while (responder && ![responder isKindOfClass:[UIViewController class]]) {
        responder = [responder nextResponder];
    }
    return (UIViewController*)responder;
}


@end


#pragma mark - Hierarchy


@implementation UIView(NIPAdditions_HierarchyAddition)

-(NSUInteger)getSubviewIndex
{
	return [self.superview.subviews indexOfObject:self];
}

-(void)bringToFront
{
	[self.superview bringSubviewToFront:self];
}

-(void)sentToBack
{
	[self.superview sendSubviewToBack:self];
}

-(void)bringOneLevelUp
{
	NSUInteger currentIndex = [self getSubviewIndex];
	[self.superview exchangeSubviewAtIndex:currentIndex withSubviewAtIndex:currentIndex+1];
}

-(void)sendOneLevelDown
{
	NSUInteger currentIndex = [self getSubviewIndex];
	[self.superview exchangeSubviewAtIndex:currentIndex withSubviewAtIndex:currentIndex-1];
}

-(BOOL)isInFront
{
	return ([self.superview.subviews lastObject] == self);
}

-(BOOL)isAtBack
{
	return ([self.superview.subviews objectAtIndex:0] == self);
}

-(void)swapDepthsWithView:(UIView*)swapView
{
	[self.superview exchangeSubviewAtIndex:[self getSubviewIndex] withSubviewAtIndex:[swapView getSubviewIndex]];
}


@end


