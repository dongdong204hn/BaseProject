- (UIView *)superviewWhichIsInstanceOfClass:(Class)class
{
    UIView *superView = self.superview;
    while (superView) {
        if ([superView isKindOfClass:class]) {
            return superView;
        }
        else {
            superView = superView.superview;
        }
    }
    return nil;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
- (UIView *)subviewWhichIsInstanceOfClass:(Class)class enumDirection:(NSEnumerationOptions)enumDirection{
    __block UIView *subView = nil;
    
    [self.subviews enumerateObjectsWithOptions:enumDirection usingBlock:^(__kindof UIView * _Nonnull obj, NSUInteger idx, BOOL * _Nonnull stop) {
        if ([obj isKindOfClass:class]) {
            subView = obj;
            *stop = YES;
        } else {
            subView = [obj subviewWhichIsInstanceOfClass:class enumDirection:enumDirection];
            if (subView) {
                *stop = YES;
            }
        }
    }];
    return subView;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
- (UIView *)superviewOrSelfWhichIsInstanceOfClass:(Class)class {
    UIView *superView = self;
    while (superView) {
        if ([superView isKindOfClass:class]) {
            return superView;
        }
        else {
            superView = superView.superview;
        }
    }
    return nil;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
- (UIView *)subviewOrSelfWhichIsInstanceOfClass:(Class)class enumDirection:(NSEnumerationOptions)enumDirection{
    if ([self isKindOfClass:class]) {
        return self;
    }
    __block UIView *subView = nil;
    
    [self.subviews enumerateObjectsWithOptions:enumDirection usingBlock:^(__kindof UIView * _Nonnull obj, NSUInteger idx, BOOL * _Nonnull stop) {
        if ([obj isKindOfClass:class]) {
            subView = obj;
            *stop = YES;
        } else {
            subView = [obj subviewWhichIsInstanceOfClass:class enumDirection:enumDirection];
            if (subView) {
                *stop = YES;
            }
        }
    }];
    return subView;
}

